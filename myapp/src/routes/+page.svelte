<h1>Search engine for Polish inflection</h1>
<h2> How to use: </h2>
<p>If you type your word of any inflection forms in the searchbox above, <br>
    then it will display the list of the words including that form. <br>
    <b>Note: you need to input all the letters of a form.<br>
    This engine does not support partial match as of now. (to be updated)</b><br>
    If you click them, then the table of inflection pops up.<br>
    The data includes both declension (for noun, adjective) and conjugation (for verb).<br>
    <br><br><br></p>

<h2>Credits: </h2>

<p>This project was inspired by the search engine for Polish declension <a href="https://odmiana.net/">Odmiana.NET</a>,<br>
    and the one for conjugation <a href="https://cooljugator.com/">Cooljugator: the Smart Verb Conjugator</a>.<br>
    Unlike these preceding works targetting either one of declension or conjugation, however,<br>
    this project covers both declension and conjugation from the single searchbox.<br></p>
<p>The data was based on <a href="https://github.com/tatuylonen/wiktextract">Wiktextract project</a>, that extracted word information from <a href="https://en.wiktionary.org">English Wiktionary</a>.<br>
